/** \brief Suma de 2 numeros
 *
 * \param a float -- Primer numero A
 * \param b float -- Segundo numero B
 * \return float -- La suma de los numeros (A+B)
 *
 */
float suma(float a,float b);

/** \brief Resta de 2 numeros
 *
 * \param a float -- Primer numero A
 * \param b float -- Segundo numero B
 * \return float -- La resta de los numeros (A-B)
 *
 */
float resta(float a, float b);

/** \brief Division de dos numeros
 *
 * \param a float -- Primer numero A
 * \param b float -- Segundo numero B
 * \return float -- La division de los numeros (A/B)
 *
 */

float division(float a, float b);

/** \brief producto entre dos numeros
 *
 * \param a float -- Primer numero A
 * \param b float -- Segundo numero B
 * \return float -- Producto entre A y B (A*B)
 *
 */
float multiplicacion(float a, float b);

/** \brief Factorial de un numero
 *
 * \param a double -- Numero A, al cual se le aplica factorial.
 * \return double -- Factorial del numero A (A!)
 *
 */
double factorial(double a);

/** \brief Validacion de un numero, si retorna 0 es entero
 *
 * \param a float -- numero a verificar
 * \return float -- retorna resta del numero flotante y casteado entero
 *
 */
float validacionEntero(float a);
